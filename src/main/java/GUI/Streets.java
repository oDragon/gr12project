    //rotate function
    public String rotate(String d){
        
        //depending on the character that is currently being displayed, change
        //the character being displayed to a different specific character
        //the character is being "rotated" clockwise
        if(d.equals(">")){
            return "v";
        } else if (d.equals("v")){
            return "<";
        } else if (d.equals("<")){
            return "^";
        }
        return ">";
    }
    
