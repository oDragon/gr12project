    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        //check that every button is displaying the correct character
        //(check that the orientation is correct)
        if(jButton2.getText().equals("v") && jButton3.getText().equals(">") &&
                jButton4.getText().equals(">") && jButton5.getText().equals("v") &&
                jButton6.getText().equals("v") && jButton7.getText().equals(">") &&
                jButton8.getText().equals("^") && jButton9.getText().equals(">") &&
                jButton10.getText().equals("^") && jButton11.getText().equals("^") &&
                jButton12.getText().equals("^")){
           
            //if they are, make the jLabel display "Puzzle Solved!"
            jLabel3.setText("Puzzle Solved!");
            //initiate the clue jDialog
            new StreetsClue(null, true).show();
            
        } else{
            
            //if they are not, make the jLabel display "Incorrect Pattern."
            jLabel3.setText("Incorrect Pattern.");
        }
    }//GEN-LAST:event_jButton13ActionPerformed

